// -----------------------------------------------------------------------------
// Build the export — flatten into NhlPlayer[] with deterministic IDs
// -----------------------------------------------------------------------------

function buildPlayer(team: string, rp: RP): NhlPlayer {
  const [name, pos] = rp;
  const base: NhlPlayer = {
    id: `pp_${team}_${slug(name)}`,
    name,
    team,
    position: pos as Position,
    goals: 0, assists: 0, pim: 0, wins: 0, shutouts: 0,
    fantasyPoints: 0,
  };
  if (pos === "G") {
    base.wins = (rp as [string, "G", number, number])[2];
    base.shutouts = (rp as [string, "G", number, number])[3];
  } else {
    const [, , g, a, pim] = rp as [string, "F" | "D", number, number, number];
    base.goals = g;
    base.assists = a;
    base.pim = pim;
  }
  base.fantasyPoints = scorePlayer(base, DEFAULT_SCORING);
  return base;
}

function slug(s: string): string {
  return s
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-zA-Z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase();
}

export const playoffPlayers: NhlPlayer[] = (() => {
  const all: NhlPlayer[] = [];
  for (const teamId of PLAYOFF_TEAM_IDS) {
    const roster = ROSTERS[teamId] ?? [];
    for (const rp of roster) all.push(buildPlayer(teamId, rp));
  }
  return all;
})();

/** Recompute fantasy points for a list of players using a different ruleset. */
export function rescore(
  players: NhlPlayer[],
  rules: ScoringRules
): NhlPlayer[] {
  return players.map((p) => ({ ...p, fantasyPoints: scorePlayer(p, rules) }));
}

